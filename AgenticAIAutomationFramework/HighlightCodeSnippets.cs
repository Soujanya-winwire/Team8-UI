    else
    {
        var isChecked = await locatorEl.IsCheckedAsync();
        if (isChecked)
        {
            await locatorEl.UncheckAsync();
        }
    }
    
    await RemoveHighlightAsync(locator); // ? ADD THIS LINE (for normal flow)
}

