/// <summary>
/// Highlights an element with a pulsing border effect during playback
/// </summary>
private async Task HighlightElementAsync(string locator)
{
    try
    {
        var selector = GetSelector(locator, "auto");
        var locatorEl = _page!.Locator(selector);
        
        // Add highlight styles with pulsing animation
        await locatorEl.EvaluateAsync(@"
            (element) => {
                if (!element) return;
                
                // Store original styles
                element.setAttribute('data-original-outline', element.style.outline || '');
                element.setAttribute('data-original-box-shadow', element.style.boxShadow || '');
                element.setAttribute('data-original-z-index', element.style.zIndex || '');
                
                // Apply highlight styles
                element.style.outline = '3px solid #FF6B6B';
                element.style.boxShadow = '0 0 20px 5px rgba(255, 107, 107, 0.6)';
                element.style.zIndex = '999999';
                element.style.transition = 'all 0.3s ease-in-out';
                
                // Create pulsing animation
                element.style.animation = 'agenticai-pulse 0.6s ease-in-out 2';
                
                // Add keyframes if not already present
                if (!document.getElementById('agenticai-pulse-animation')) {
                    const style = document.createElement('style');
                    style.id = 'agenticai-pulse-animation';
                    style.textContent = `
                        @keyframes agenticai-pulse {
                            0% { 
                                outline-color: #FF6B6B; 
                                box-shadow: 0 0 20px 5px rgba(255, 107, 107, 0.6);
                            }
                            50% { 
                                outline-color: #4ECDC4; 
                                box-shadow: 0 0 30px 10px rgba(78, 205, 196, 0.8);
                            }
                            100% { 
                                outline-color: #FF6B6B; 
                                box-shadow: 0 0 20px 5px rgba(255, 107, 107, 0.6);
                            }
                        }
                    `;
                    document.head.appendChild(style);
                }
            }
        ");
        
        // Keep highlight visible for 800ms
        await Task.Delay(800);
    }
    catch (Exception ex)
    {
        Logger.Debug($"Could not highlight element '{locator}': {ex.Message}");
    }
}

/// <summary>
/// Removes highlight from an element
/// </summary>
private async Task RemoveHighlightAsync(string locator)
{
    try
    {
        var selector = GetSelector(locator, "auto");
        var locatorEl = _page!.Locator(selector);
        
        await locatorEl.EvaluateAsync(@"
            (element) => {
                if (!element) return;
                
                // Restore original styles
                element.style.outline = element.getAttribute('data-original-outline') || '';
                element.style.boxShadow = element.getAttribute('data-original-box-shadow') || '';
                element.style.zIndex = element.getAttribute('data-original-z-index') || '';
                element.style.animation = '';
                
                // Remove stored attributes
                element.removeAttribute('data-original-outline');
                element.removeAttribute('data-original-box-shadow');
                element.removeAttribute('data-original-z-index');
            }
        ");
    }
    catch (Exception ex)
    {
        Logger.Debug($"Could not remove highlight from element '{locator}': {ex.Message}");
    }
}

