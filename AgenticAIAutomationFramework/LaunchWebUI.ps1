#!/usr/bin/env pwsh

Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?   ?? Agentic AI - Web Test Management Platform       ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Check if .NET is installed
Write-Host "?? Checking prerequisites..." -ForegroundColor Yellow
$dotnetVersion = dotnet --version 2>$null
if ($LASTEXITCODE -ne 0) {
    Write-Host "? .NET SDK not found!" -ForegroundColor Red
    Write-Host "Please install .NET 9 SDK from: https://dotnet.microsoft.com/download" -ForegroundColor Yellow
    pause
    exit 1
}

Write-Host "? .NET SDK $dotnetVersion found" -ForegroundColor Green

# Navigate to Web UI project
$webUIPath = Join-Path $PSScriptRoot "tools\AgenticAI.WebUI"
if (-not (Test-Path $webUIPath)) {
    Write-Host "? Web UI project not found at: $webUIPath" -ForegroundColor Red
    pause
    exit 1
}

Set-Location $webUIPath
Write-Host "?? Location: $webUIPath" -ForegroundColor Cyan
Write-Host ""

# Build the project
Write-Host "?? Building Web UI project..." -ForegroundColor Yellow
dotnet build --configuration Release -v quiet
if ($LASTEXITCODE -ne 0) {
    Write-Host "? Build failed!" -ForegroundColor Red
    Write-Host "Run 'dotnet build' manually to see detailed errors" -ForegroundColor Yellow
    pause
    exit 1
}

Write-Host "? Build successful" -ForegroundColor Green
Write-Host ""

# Check if port 5000 is available
Write-Host "?? Checking port availability..." -ForegroundColor Yellow
$portInUse = Get-NetTCPConnection -LocalPort 5000 -ErrorAction SilentlyContinue
if ($portInUse) {
    Write-Host "??  Port 5000 is already in use!" -ForegroundColor Yellow
    Write-Host "The web server will try to use an alternative port" -ForegroundColor Yellow
    Write-Host ""
}

# Display startup information
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?? Starting Web Test Management Platform..." -ForegroundColor Green
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""
Write-Host "Access the platform at:" -ForegroundColor Yellow
Write-Host "  ?? Web UI: http://localhost:5000" -ForegroundColor Cyan
Write-Host "  ?? API: http://localhost:5000/swagger" -ForegroundColor Cyan
Write-Host "  ?? SignalR: ws://localhost:5000/testExecutionHub" -ForegroundColor Cyan
Write-Host ""
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "Features:" -ForegroundColor Yellow
Write-Host "  ? Create tests visually (no coding)" -ForegroundColor White
Write-Host "  ? Execute tests in real-time" -ForegroundColor White
Write-Host "  ? View live console output" -ForegroundColor White
Write-Host "  ? Manage test scenarios" -ForegroundColor White
Write-Host "  ? Configure framework settings" -ForegroundColor White
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""
Write-Host "Press Ctrl+C to stop the server" -ForegroundColor Yellow
Write-Host ""

# Wait a moment before opening browser
Start-Sleep -Seconds 2

# Open browser in background
Write-Host "?? Opening browser..." -ForegroundColor Cyan
Start-Process "http://localhost:5000"

Write-Host ""
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "Server starting..." -ForegroundColor Green
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Run the application
dotnet run --no-build --configuration Release

# If we get here, the server stopped
Write-Host ""
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "Server stopped" -ForegroundColor Yellow
Write-Host "????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""
Write-Host "Press any key to exit..."
$null = $Host.UI.RawUI.ReadKey("NoEcho,IncludeKeyDown")
